
package metrictoimperialv2;

import static java.lang.System.out;


public class Volume {
    
     
   double x ; // cubic meters
   double y = Math.pow(3.28 , 3)  ; //cubic feet
   double a = Math.pow(1.09 , 3) ;//cubic yards
   double b = Math.pow(39.37 , 3) ;  //cubic inches 
   
   public void setMeters() {
       this.x = x ;
   }
   
    public double feetConvert() {
       x = x * y ; 
       return x ; 
   }
   
   public double yardConvert(){
       x = x * a; 
       return x ; 
   }
   
   public double inchesConvert(){
       x = x * b ;
       return x ; 
   }
   
   public void display(){
       out.println(x);
   }
   
   
}
