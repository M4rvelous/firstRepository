
package metrictoimperialv2;

import static java.lang.System.out;


public class temperature {

    double x ; // Celcius 
    double y = 33.80 ; // fahrenheit 
    
    public void setCelcius() {
       this.x = x ;
   }
    
   public double fahrConvert() {
       x = x * y ; 
       return x ; 
   }
   
   public void display(){
       out.println(x);
   }
   
   
}
